

#range function consists three prarameters
'''1st parameter is start value
2nd parameter is end value
3rd parameter is step signify difference'''


#end value:10
n=list(range(10))
print(n)


#start value:2
#end value:11
n=list(range(2,11))
print(n)

#start value : 2
#end value: 11
#step: 2
n=list(range(2,11,2))
print(n)